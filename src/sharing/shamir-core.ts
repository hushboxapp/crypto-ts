/** GF(2^8) addition (XOR). Also serves as subtraction. */
function add(a: number, b: number): number {
  return (a ^ b) & FIELD_MASK;
}

/** GF(2^8) multiplication via log/exp tables. Returns 0 if either operand is 0. */
function mult(a: number, b: number): number {
  if (a === 0 || b === 0) return 0;
  const sum = (LOG_TABLE[a]! + LOG_TABLE[b]!) % FIELD_MASK;
  return EXP_TABLE[sum]!;
}

/** GF(2^8) division via log/exp tables. Throws on divide by zero. */
function div(a: number, b: number): number {
  if (b === 0) throw new Error('shamir: cannot divide by zero');
  if (a === 0) return 0;
  const diff = (LOG_TABLE[a]! - LOG_TABLE[b]! + FIELD_MASK) % FIELD_MASK;
  return EXP_TABLE[diff]!;
}

/** Lagrange-interpolates the polynomial defined by samples and returns its value at `x`. */
function interpolatePolynomial(xSamples: Uint8Array, ySamples: Uint8Array, x: number): number {
  const limit = xSamples.length;
  let result = 0;

  for (let i = 0; i < limit; i++) {
    let basis = 1;
    for (let j = 0; j < limit; j++) {
      if (i === j) continue;
      const num = add(x, xSamples[j]!);
      const denom = add(xSamples[i]!, xSamples[j]!);
      basis = mult(basis, div(num, denom));
    }
    result = add(result, mult(ySamples[i]!, basis));
  }

  return result;
}

/** Evaluates polynomial `coefficients` at `x` using Horner's method. */
function evaluate(coefficients: Uint8Array, x: number, degree: number): number {
  if (x === 0) throw new Error('shamir: cannot evaluate secret polynomial at zero');
  let result = coefficients[degree]!;
  for (let i = degree - 1; i >= 0; i--) {
    result = add(mult(result, x), coefficients[i]!);
  }
  return result;
}

/**
 * Generates `degree + 1` polynomial coefficients with `intercept` as the
 * constant term and `degree` random coefficients above it.
 */
function newCoefficients(
  intercept: number,
  degree: number,
  randomBytes: (n: number) => Uint8Array,
): Uint8Array {
  const coefficients = new Uint8Array(degree + 1);
  coefficients[0] = intercept;
  coefficients.set(randomBytes(degree), 1);
  return coefficients;
}

/**
 * Returns the integers `[1, 256)` in a pseudo-random order. The shuffle is
 * intentionally biased — the upstream comment notes (and the Shamir scheme
 * confirms) that share security does not depend on uniform x-coordinate
 * selection. Distinctness is what matters, and that is preserved by the
 * in-place swap.
 */
function newCoordinates(randomBytes: (n: number) => Uint8Array): Uint8Array {
  const coordinates = new Uint8Array(FIELD_MASK);
  for (let i = 0; i < FIELD_MASK; i++) coordinates[i] = i + 1;

  const indices = randomBytes(FIELD_MASK);
  for (let i = 0; i < FIELD_MASK; i++) {
    const j = indices[i]! % FIELD_MASK;
    const tmp = coordinates[i]!;
    coordinates[i] = coordinates[j]!;
    coordinates[j] = tmp;
  }
  return coordinates;
}

/**
 * Splits `secret` into `shares` Shamir shares with the given `threshold`.
 *
 * Caller-side validation (non-empty secret, integer shares/threshold in
 * range, `threshold <= shares`) is intentionally not duplicated here — the
 * library's `ShamirProvider` wraps this and emits typed errors.
 */
export function splitSecret(
  secret: Uint8Array,
  shares: number,
  threshold: number,
  randomBytes: (n: number) => Uint8Array,
): Uint8Array[] {
  const result: Uint8Array[] = [];
  const secretLength = secret.byteLength;
  const xCoordinates = newCoordinates(randomBytes);

  for (let i = 0; i < shares; i++) {
    const share = new Uint8Array(secretLength + 1);
    share[secretLength] = xCoordinates[i]!;
    result.push(share);
  }

  const degree = threshold - 1;
  for (let i = 0; i < secretLength; i++) {
    const coefficients = newCoefficients(secret[i]!, degree, randomBytes);
    for (let j = 0; j < shares; j++) {
      result[j]![i] = evaluate(coefficients, xCoordinates[j]!, degree);
    }
  }

  return result;
}

/**
 * Reconstructs the secret from `shares`. Each share's last byte carries its
 * x-coordinate; the remaining bytes are the polynomial values.
 *
 * @throws If two shares share an x-coordinate, or if shares disagree on length.
 */
export function combineShares(shares: Uint8Array[]): Uint8Array {
  if (shares.length < 2) {
    throw new Error('shamir: need at least 2 shares to combine');
  }
  const shareLength = shares[0]!.byteLength;
  if (shareLength < 2) {
    throw new Error('shamir: each share must be at least 2 bytes');
  }
  for (const share of shares) {
    if (share.byteLength !== shareLength) {
      throw new Error('shamir: all shares must have the same byte length');
    }
  }

  const sharesLength = shares.length;
  const secretLength = shareLength - 1;
  const secret = new Uint8Array(secretLength);

  const xSamples = new Uint8Array(sharesLength);
  const ySamples = new Uint8Array(sharesLength);

  const seen = new Set<number>();
  for (let i = 0; i < sharesLength; i++) {
    const sample = shares[i]![shareLength - 1]!;
    if (seen.has(sample)) {
      throw new Error('shamir: shares must contain unique x-coordinates');
    }
    seen.add(sample);
    xSamples[i] = sample;
  }

  for (let i = 0; i < secretLength; i++) {
    for (let j = 0; j < sharesLength; j++) {
      ySamples[j] = shares[j]![i]!;
    }
    secret[i] = interpolatePolynomial(xSamples, ySamples, 0);
  }

  return secret;
}
