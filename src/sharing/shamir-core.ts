/**
 * Shamir's Secret Sharing core, vendored from `shamir-secret-sharing@0.0.4`
 * (Apache-2.0, privy-io/shamir-secret-sharing). Vendoring eliminates a 0.x
 * supply-chain dependency and routes randomness through the caller-supplied
 * source so the library has a single CSPRNG audit point.
 *
 * Operates over GF(2^8) using the polynomial x^8 + x^4 + x^3 + x + 1.
 * Lookup tables and generator (0xe5) match the HashiCorp Vault Shamir
 * implementation at the time of import.
 *
 * Notes on side-channels:
 * - {@link mult} and {@link div} branch on a zero operand. This leaks
 *   information about share-byte values via timing. For {@link split} the
 *   leaked values are the secret itself; for {@link combine} they are the
 *   already-known share bytes. Both flows in this library wrap shares in
 *   AES-GCM with AAD before persistence, so a network attacker observes
 *   only ciphertext. A local timing-side-channel attacker has stronger
 *   primitives available; we accept the leak.
 */

